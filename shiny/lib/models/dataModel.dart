  void addToFavorite(String itemId) async {
    List<String> _favoriteRecipeList = [];
    SharedPreferences prefs = await SharedPreferences.getInstance();

    bool _seen = (prefs.getBool('favoriteFirst') ?? false);

    if (_seen) {
      _favoriteRecipeList = prefs.getStringList('favorite') ?? [];

      if (!_favoriteRecipeList.contains(itemId)) {
        _favoriteRecipeList.add(itemId);
        prefs.setStringList('favorite', _favoriteRecipeList);
        notifyListeners();
      }
    } else {
      await prefs.setBool('favoriteFirst', true);
      prefs.setStringList('favorite', []);
      _favoriteRecipeList.add(itemId);
      prefs.setStringList('favorite', _favoriteRecipeList);
    }

    notifyListeners();
  }

