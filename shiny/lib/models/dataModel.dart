  double bmi = 0;

  double calculateBmi(int height, int weight) {
    return bmi = weight / pow(height / 100, 2);
  }

  String bmiNotation(double bmi) {
    if (bmi > 30) {
      return 'سمنة مفرطة!';
    }
    if (bmi > 25) {
      return 'سمنة';
    } else if (bmi > 18.5) {
      return 'وزن طبيعي';
    } else if (bmi > 16) {
      return 'نحافة';
    } else
      return 'نحافة مفرطة';
  }

  String bmiAdvice(double bmi) {
    if (bmi > 30) {
      return 'أنت تعانين من السمنة المفرطة، احذري! فوصولك لهذه الدرجة قد يجعلك معرضة للعديد من المشاكل الصحية';
    }
    if (bmi > 25) {
      return 'يبدو لنه لديك سمنة ينصح بممارسة التمارين الرياضية والإبتعاد قدر المستطاع عن الأكل الغني بالدهون والسعرات الحرارية';
    } else if (bmi > 18.5) {
      return 'ان وزنك طبيعي، حاولي المحافظة عليه بالنوم المنتظم والأكل الصحي بإستهلاك كفايتك من السعرات الحرارية مع ممارسة التمارين الرياضية';
    } else if (bmi > 16) {
      return 'يمكنك زيادة وزنك  بزيادة السعرات الحرارية عنطريق الأكل الصحي المتوازن وممارسة التمارين الرياضية';
    } else
      return 'يبدو انك تعانين من نحافة مفرطة والتي قد تؤدي الا مشاكل صحية تحتاجين الى التغذية الصحية لزيادة وزنك';
  }

  void showSnakBare(bool addORremove, BuildContext context) {
    if (addORremove) {
      final snackBar = SnackBar(
        backgroundColor: Colors.green[200],
        content: Text(
          'تمت الإضافة للمفضلة',
          textAlign: TextAlign.center,
        ),
        duration: Duration(milliseconds: 500),
      );
      ScaffoldMessenger.of(context)
        ..hideCurrentSnackBar()
        ..showSnackBar(snackBar);
    } else {
      final snackBar = SnackBar(
        backgroundColor: Colors.red[200],
        content: Text(
          'تمت الإزالة من المفضلة',
          textAlign: TextAlign.center,
        ),
        duration: Duration(milliseconds: 500),
      );
      ScaffoldMessenger.of(context)
        ..hideCurrentSnackBar()
        ..showSnackBar(snackBar);
    }
  }
